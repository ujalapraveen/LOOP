# Ek program likho jo loop ka use kar ke 1 se leke 100 tak saare numbers ka sum print kare.

# Aapke program ko pehle 1 se 100 ke beech mein saare numbers ka sum calculate karna hai. 1+2+3+4 +5+6+7....+95+96+97+98+99+100 Fir uss sum ko print karana hai.

i=0
sum=0
while i<=100:
    sum+=i
    i+=1
print(sum)